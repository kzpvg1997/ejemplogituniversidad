    /**
     * Registramos una asignatura
     */
    private void btnRegistrarAsignatureaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarAsignatureaActionPerformed
        String cedula = tfcedulaestudiante.getText();
        String codigo = tfcodigoasignatura.getText();
    	if(codigo.isEmpty() || cedula.isEmpty()){
    		JOptionPane.showMessageDialog(null, "Por favor, busque el estudiante\nY la asignatura a registrar", "Administrador Registro Curso", JOptionPane.WARNING_MESSAGE);   
        }else{
        	try {
        		if(tablaHorario.getSelectedRow() != -1){
	                DefaultTableModel tm = (DefaultTableModel) tablaHorario.getModel(); 
	                String dato=String.valueOf(tm.getValueAt(tablaHorario.getSelectedRow(),0));
	                int codigoCurso = Integer.parseInt(dato);
					Estudiante e = controlador.buscarEstudiante(cedula);
					Curso c = controlador.buscarCurso(codigoCurso);
					if(c != null && e != null){
						RegistroCurso rc = new RegistroCurso(e, c);
						controlador.registrarCurso(rc);
			    		JOptionPane.showMessageDialog(null, "Se ha registrado en el curso correctamente", "Administrador Registro Curso", JOptionPane.QUESTION_MESSAGE);   
			    		int sumaCreditos = controlador.sumaCreditosByAlumno(e);
						tfcreditosregistradosestudiante.setText(String.valueOf(sumaCreditos));
					}else{
			    		JOptionPane.showMessageDialog(null, "No se ha encontrado el curso de la asignatura y/o el estudiante", "Administrador Registro Curso", JOptionPane.WARNING_MESSAGE);   
					}
        		}else{
		    		JOptionPane.showMessageDialog(null, "Por favor, seleccione un curso", "Administrador Registro Curso", JOptionPane.WARNING_MESSAGE);   
        		}
			}catch (ExcepcionNegocio ex) {
	    		JOptionPane.showMessageDialog(null, ex.getMessage(), "Administrador Registro Curso", JOptionPane.WARNING_MESSAGE);   
			}catch (Exception e) {
				e.printStackTrace();
	    		JOptionPane.showMessageDialog(null, "Ups! ha ocurrido un error.", "Administrador Registro Curso", JOptionPane.ERROR_MESSAGE);   
			}
        	
        }
    }

