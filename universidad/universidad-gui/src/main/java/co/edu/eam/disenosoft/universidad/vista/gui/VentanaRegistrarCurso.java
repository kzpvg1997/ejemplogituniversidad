    /**
     * Buscamos un Estudiante
     */
    private void btnBuscarEstuainteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarEstuainteActionPerformed
    	String cedula = tfcedulaestudiante.getText();
    	if(!cedula.isEmpty()){
    		try {
				Estudiante estudiante = controlador.buscarEstudiante(cedula);
				if(estudiante != null){
					tfnombreestudiante.setText(estudiante.getNombre()+" "+estudiante.getApellido());
					int sumaCreditos = controlador.sumaCreditosByAlumno(estudiante);
					tfcreditosregistradosestudiante.setText(String.valueOf(sumaCreditos));
				}else{
		    		JOptionPane.showMessageDialog(null, "No se ha encontrado ningun estudiante\ncon el numero de cedula "+cedula, "Administrador Registro Curso", JOptionPane.WARNING_MESSAGE);   
				}
			} catch (Exception e) {
				e.printStackTrace();
	    		JOptionPane.showMessageDialog(null, "Ups! ha ocurrido un error.", "Administrador Registro Curso", JOptionPane.ERROR_MESSAGE);   
			}
    	}else{
    		JOptionPane.showMessageDialog(null, "por favor, ingrese la cedula del estudiante a buscar", "Administrador Curso", JOptionPane.QUESTION_MESSAGE);  
    	}
    }
    /**
     * Buscamos la asignatura y mostramos sus datos
     */
    private void btnBuscarAsignaturaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarAsignaturaActionPerformed
    	String codigoAsignatura = tfcodigoasignatura.getText();
    	if(!codigoAsignatura.isEmpty()){
    		try {
				Asignatura asignatura = controlador.buscarAsignatura(codigoAsignatura);
				if(asignatura != null){
					tfnombreasignatura.setText(asignatura.getNombre());
					tfcreditosasignatura.setText(String.valueOf(asignatura.getNumeroCreditos()));
					tfTipoAsignatura.setText(asignatura.getTipo().name());
					llenarTablaRegistros(asignatura);
				}else{
					JOptionPane.showMessageDialog(null, "No se ha encontrado ninguna asignatura\ncon el codigo "+codigoAsignatura, "Administrador Registro Curso", JOptionPane.QUESTION_MESSAGE);   
				}
			} catch (Exception e) {
				e.printStackTrace();
	    		JOptionPane.showMessageDialog(null, "Ups! ha ocurrido un error.", "Administrador Registro Curso", JOptionPane.ERROR_MESSAGE);   
			}
    	}else{
    		JOptionPane.showMessageDialog(null, "por favor, ingrese el codigo de la asignatura a buscar", "Administrador Registro Curso", JOptionPane.WARNING_MESSAGE);  
    	}    
    }
    /* Quitar Asignatura Registrada */
    private void btnQuitarAsignaturaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnQuitarAsignaturaActionPerformed
        String cedula = tfcedulaestudiante.getText();
        String codigo = tfcodigoasignatura.getText();
        if(cedula.isEmpty() || codigo.isEmpty()){
    		JOptionPane.showMessageDialog(null, "por favor, busque el Estudiante y/o la Asignatura", "Administrador Registro Curso", JOptionPane.WARNING_MESSAGE);  
        }else{
        	try {
				Estudiante e = controlador.buscarEstudiante(cedula);
				RegistroCurso rc = controlador.buscarRegistroCurso(e, codigo);
				if(rc != null){
					controlador.eliminarRegistroCurso(rc);
		    		JOptionPane.showMessageDialog(null, "Se ha Eliminado Correctamente de tu registro", "Administrador Registro Curso", JOptionPane.QUESTION_MESSAGE);  
		    		int sumaCreditos = controlador.sumaCreditosByAlumno(e);
					tfcreditosregistradosestudiante.setText(String.valueOf(sumaCreditos));
				}else{
		    		JOptionPane.showMessageDialog(null, "No se ha encontrado ningun Registro curso con esta Asignatura\nAl estudiante "+e.getNombre()+" "+e.getApellido(), "Administrador Registro Curso", JOptionPane.WARNING_MESSAGE);  
				}
			} catch (Exception e) {
	    		JOptionPane.showMessageDialog(null, "Ups! ha ocurrido un error.", "Administrador Registro Curso", JOptionPane.ERROR_MESSAGE);   
				e.printStackTrace();
			}
        }
    }
    public void cargarCursosAsignatura(Asignatura asignatura){
    	try {
    		DefaultTableModel table = (DefaultTableModel) tablaHorario.getModel();
    		table.setRowCount(0);
    		List<Curso> cursos = controlador.listaCursosByMateria(asignatura);
    		if(cursos.size() > 0){
				for (Curso curso : cursos) {
					table.addRow(new Object[]{curso.getCodigo(),curso.getSesiones()});
				}
    		}
		} catch (Exception e) {
			e.printStackTrace();
		}
    }
    public void llenarTablaRegistros(Asignatura asi){
    	try {
    	DefaultTableModel dtm = (DefaultTableModel) tablaHorario.getModel();
    	dtm.setRowCount(0);
    	
    	
    		List<Curso> cursos = controlador.listaCursosByMateria(asi);
			for (Curso curso : cursos) {
				
				String sesiones = ""; 
				
						
				for(int i=0;i<curso.getSesiones().size();i++){
				
					sesiones+= curso.getSesiones().get(i).getDia() +" ";
					sesiones+= curso.getSesiones().get(i).getHoraInicio() +"-";
					sesiones+= curso.getSesiones().get(i).getHoraFinal() +". ";

				}
				
			dtm.addRow(new Object[]{curso.getCodigo(),sesiones});
				
			}
			
		} catch (Exception e) {
			// TODO Auto-generated catch block
			e.printStackTrace();
		}
    }
    
    
