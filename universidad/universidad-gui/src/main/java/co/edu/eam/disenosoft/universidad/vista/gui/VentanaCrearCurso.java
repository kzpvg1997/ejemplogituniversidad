    private void btnBuscarAsignaturaActionPerformed(java.awt.event.ActionEvent evt) throws Exception {                                                    
        String codigoAsignatura = tfcodigoasignatura.getText();
        try{
	        Asignatura asignatura = conAsig.buscar(codigoAsignatura);
	        if(asignatura != null){
	        	tfnombreasignatura.setText(asignatura.getNombre());
	        }else{
	    		JOptionPane.showMessageDialog(null, "No se ha encontrado ninguna asignatura con el codigo "+codigoAsignatura, "Administrador Curso", JOptionPane.QUESTION_MESSAGE); 
	    		limpiarCamposAsignatura();
	        }
    	}catch(Exception e){
    		e.printStackTrace();
    		JOptionPane.showMessageDialog(null, "Ups! ha ocurrido un error.", "Administrador Curso", JOptionPane.ERROR_MESSAGE);   
    	}
    }
    private void btnBuscarDocenteActionPerformed(java.awt.event.ActionEvent evt) {                                                     
        String cedulaDocente = tfceduladocente.getText();
        try{
	        Docente docente = conDocente.buscar(cedulaDocente);
	        if(docente != null){
	        	tfnombredocente.setText(docente.getNombre());
	        }else{
	    		JOptionPane.showMessageDialog(null, "No se ha encontrado ninguna docente con la cedula "+cedulaDocente, "Administrador Curso", JOptionPane.QUESTION_MESSAGE);  
	    		limpiarCamposDocente();
	        }
    	}catch (ExcepcionNegocio ex){
    		JOptionPane.showMessageDialog(null, ex.getMessage(), "ERROR", JOptionPane.ERROR_MESSAGE);   
    	}
        catch(Exception e){
    		e.printStackTrace();
    		JOptionPane.showMessageDialog(null, "Ups! ha ocurrido un error.", "Administrador Curso", JOptionPane.ERROR_MESSAGE);   
    	}
    }
    private void btnAgregarSesionActionPerformed(java.awt.event.ActionEvent evt) {                                                 
        if(comboCursos.getSelectedIndex() != 0){
	    	Curso curso = (Curso) comboCursos.getSelectedItem();
	        DiaEnum dia = (DiaEnum) comboDia.getSelectedItem();
	        int horaFinal = Integer.parseInt((String) comboHoraFin.getSelectedItem());
	        int horaInicio = Integer.parseInt((String) comboHoraInicio.getSelectedItem());
	        SesionCurso sesion = new SesionCurso(0, dia, horaFinal, horaInicio, curso);
	        try {
				if(Integer.parseInt((String) comboHoraFin.getSelectedItem())< Integer.parseInt((String) comboHoraInicio.getSelectedItem())){
		    		JOptionPane.showMessageDialog(null, "La hora final debe ser mayor a la hora inicial", "Administrador Curso", JOptionPane.ERROR_MESSAGE);  
				}else{
	        	controlador.crearSesion(sesion);
	    		JOptionPane.showMessageDialog(null, "Se ha Creado la Sesion", "Administrador Curso", JOptionPane.QUESTION_MESSAGE);  
	    		cargarSesiones(curso);
	    		resetSesion();
				
				}
	        }catch (ExcepcionNegocio ex){
	    		JOptionPane.showMessageDialog(null, ex.getMessage(), "ERROR", JOptionPane.ERROR_MESSAGE);   
	    	} catch (Exception e) {
				e.printStackTrace();
	    		JOptionPane.showMessageDialog(null, "Ups! ha ocurrido un error.", "Administrador Curso", JOptionPane.ERROR_MESSAGE);   
			}
        }else{
    		JOptionPane.showMessageDialog(null, "Por favor, seleccione el curso", "Administrador Curso", JOptionPane.ERROR_MESSAGE);   
        }
    }
    private void btnCrearActionPerformed(java.awt.event.ActionEvent evt) throws Exception {//GEN-FIRST:event_btnCrearActionPerformed
        String codigoAsignatura = tfcodigoasignatura.getText();
        String cedulaDocente = tfceduladocente.getText();
        try{
	        if(codigoAsignatura.isEmpty() || cedulaDocente.isEmpty()){
	    		JOptionPane.showMessageDialog(null, "Por favor, Busque la Asignatura y el docente\npara poder crear el curso", "Administrador Curso", JOptionPane.QUESTION_MESSAGE);  
	        }else{
	        	Docente docente= conDocente.buscar(cedulaDocente);
	            Asignatura asignatura = conAsig.buscar(codigoAsignatura);
	            if(docente != null && asignatura != null){
	            	Curso curso = new Curso(0, docente, asignatura);
	            	controlador.crearCurso(curso);
	        		JOptionPane.showMessageDialog(null, "Se ha Creado el curso", "Administrador Curso", JOptionPane.QUESTION_MESSAGE);  
	        		cargarComboCursos();
	        		limpiarCamposAsignatura();
	        		limpiarCamposDocente();
	            }else{
	        		JOptionPane.showMessageDialog(null, "El docente y/o asignatura no existen", "Administrador Curso", JOptionPane.QUESTION_MESSAGE);  
	            }
	        }
    	}catch (ExcepcionNegocio ex){
    		JOptionPane.showMessageDialog(null, ex.getMessage(), "ERROR", JOptionPane.ERROR_MESSAGE);   
    	}catch(Exception e){
    		e.printStackTrace();
    		JOptionPane.showMessageDialog(null, "Ups! ha ocurrido un error.", "Administrador Curso", JOptionPane.ERROR_MESSAGE);   
    	}
        
    }//GEN-LAST:event_btnCrearActionPerformed

