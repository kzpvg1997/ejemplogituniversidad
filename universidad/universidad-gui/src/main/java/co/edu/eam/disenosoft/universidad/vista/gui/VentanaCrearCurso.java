    private void comboCursosItemStateChanged(java.awt.event.ItemEvent evt) {                                             
		if(comboCursos.getSelectedIndex() != 0 && evt.getStateChange() == ItemEvent.SELECTED){
			try {
				Curso curso = (Curso) comboCursos.getSelectedItem();
				Asignatura asignatura = conAsig.buscar(curso.getAsignatura().getCodigo());
				if(asignatura != null){
					tfcodigoasignatura.setText(asignatura.getCodigo());
					tfnombreasignatura.setText(asignatura.getNombre());
				}else{
		    		JOptionPane.showMessageDialog(null, "Este curso no tiene Asignado una Asignatura", "Administrador Curso", JOptionPane.ERROR_MESSAGE);   
				}
				Docente docente = conDocente.buscar(curso.getDocente().getCedula());
				if(docente != null){
					tfceduladocente.setText(docente.getCedula());
					tfnombredocente.setText(docente.getNombre());
				}else{
		    		JOptionPane.showMessageDialog(null, "Este curso no tiene Asignado un Docente", "Administrador Curso", JOptionPane.ERROR_MESSAGE);   
				}
				cargarSesiones(curso);
			} catch (ExcepcionNegocio ex){
	    		JOptionPane.showMessageDialog(null, ex.getMessage(), "ERROR", JOptionPane.ERROR_MESSAGE);   
	    	}catch (Exception e) {
				e.printStackTrace();
			}
		}
    }

