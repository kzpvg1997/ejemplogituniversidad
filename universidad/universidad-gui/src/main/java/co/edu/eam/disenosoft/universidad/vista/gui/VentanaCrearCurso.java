    public void cargarComboCursos(){
		try {
	    	comboCursos.removeAllItems();
	    	comboCursos.addItem("Nuevo Curso o seleccione");
	    	List<Curso> lista;
			lista = controlador.listarCursos();
	        for(int i=0;i<lista.size();i++){
	        	comboCursos.addItem(lista.get(i));
	        }
		} catch (Exception e) {
			e.printStackTrace();
		} 
    }
    public void resetSesion(){
    	comboDia.setSelectedIndex(0);
    	comboHoraFin.setSelectedIndex(0);
    	comboHoraInicio.setSelectedIndex(0);
    }
    public void cargarSesiones(Curso curso){
    	try {
    		DefaultTableModel table = (DefaultTableModel) tablaHorario.getModel();
    		table.setRowCount(0);
    		List<SesionCurso> sesiones = controlador.listarSesionesCurso(curso);
    		if(sesiones.size() > 0){
				for (SesionCurso sesionCurso : sesiones) {
					table.addRow(new Object[]{sesionCurso.getDia(),sesionCurso.getHoraInicio(),sesionCurso.getHoraFinal()});
				}
    		}
		} catch (Exception e) {
			e.printStackTrace();
		}
    	
    }
    
